package javabasic;

public class Example02 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;

        System.out.println("10 + 5 = " + (num1 + num2));
        System.out.println("10 - 5 = " + (num1 - num2));
        System.out.println("10 * 5 = " + (num1 * num2));
        System.out.println("10 / 5 = " + (num1 / num2));

        System.out.println(num1 + num2);

    }
}
